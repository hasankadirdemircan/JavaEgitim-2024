package java_library_methods;

import static java.lang.Math.*;

public class JavaLibraryMethodsExample01 {
    public static void main(String[] args) {
        System.out.println(" Math.sqrt() : " + sqrt(25));
        System.out.println(" Math.max() : " +  max(5,11));
        System.out.println(" Math.abs() : " +  abs(-5));
        System.out.println(" Math.pow() : "   +  Math.pow(5, 3));

    }
}
