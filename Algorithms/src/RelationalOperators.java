public class RelationalOperators {
    public static void main(String[] args) {
        int number1 = 5;
        int number2 = 5;

        boolean greater = number1 > number2;
        System.out.println("number1 > number2 : " + greater);

        System.out.println("number1 < number2 : " + (number1 < number2));

        System.out.println("number1 <= number2 : " + (number1 <= number2));

        System.out.println("number1 == number2 : " + (number1 == number2));

        System.out.println("number1 != number2 : " + (number1 != number2));
    }
}
